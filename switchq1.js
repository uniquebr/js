function DayIdentifier(a){
  switch  (a){
    case 1:
      console.log("Monday");
      break;
    case 2:
        console.log("Thuesday");
        break;
    case 3:
      console.log("Wednesday");
      break;
    case 4:
        console.log("Thrusday");
        break;
    case 5:
        console.log("Friday");
        break;
    case 6:
      console.log("Saturday");
      break;
    case 7:
    console.log("Sunday");
    break;
  }
}
DayIdentifier(5)